function i_drawband(coord,color,row)
x=[24:-1:1];
row=x(row);
nb = size(coord,1);  %  number of boxplots
hw = 0.1;   %  halfwidth of boxes 0.15
colorset=[.5 .5 .5;
	   1 1 1;
	   0 0 0;   % black
 	   .7 .7 .7;
	   .2 .2 .2;
	   .4 .4 .4;
	   .5 .5 .7;
	   .7 .5 .5;
	    1 0 0; % for ENCODE regions
	    0.50196 1 1]; % for centromere
hold on
for ii = 1:nb
   temp1 = coord(ii,1);
   temp2 = coord(ii,2);
   xx = [temp1 temp1 temp2 temp2 temp1];
if(color(ii)==9)
    hw=0.08;
    yy = [row-hw row+hw row+hw row-hw row-hw];
else
    yy = [row-hw row+hw row+hw row-hw row-hw];
end
    plot(xx,yy,'-')


    fill(xx,yy,colorset(color(ii),:))
end

